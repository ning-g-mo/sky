package com.mengze.sky.model;

/**
 * 装扮数据
 */
public class Adorn {
    /**
     * 裤子
     */
    private String body;
    /**
     * 脸饰
     */
    private String face;
    /**
     * 鞋子
     */
    private String feet;
    /**
     * 头发
     */
    private String hair;
    /**
     * 发饰
     */
    private String hat;
    /**
     * 角
     */
    private String horn;
    /**
     * 面具
     */
    private String mask;
    /**
     * 项链
     */
    private long neck;
    /**
     * 背饰
     */
    private long prop;
    /**
     * 斗篷
     */
    private String wing;

    public String getBody() { return body; }
    public void setBody(String value) { this.body = value; }

    public String getFace() { return face; }
    public void setFace(String value) { this.face = value; }

    public String getFeet() { return feet; }
    public void setFeet(String value) { this.feet = value; }

    public String getHair() { return hair; }
    public void setHair(String value) { this.hair = value; }

    public String getHat() { return hat; }
    public void setHat(String value) { this.hat = value; }

    public String getHorn() { return horn; }
    public void setHorn(String value) { this.horn = value; }

    public String getMask() { return mask; }
    public void setMask(String value) { this.mask = value; }

    public long getNeck() { return neck; }
    public void setNeck(long value) { this.neck = value; }

    public long getProp() { return prop; }
    public void setProp(long value) { this.prop = value; }

    public String getWing() { return wing; }
    public void setWing(String value) { this.wing = value; }
}